// 両ツールを analysis-sample に対して実行し、実出力を fixture 化する。
//   - src/data/analysisFixtures.ts : 射影した生JSON + CLI文言 + 実行時間
//   - src/data/sample.ts           : 表示用のサンプルコード（解析対象と同一）
// 使い方: `node scripts/capture-analysis.mjs`（プロジェクトルートで実行）

import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SAMPLE_DIR = "analysis-sample";
const FALLOW = join(ROOT, "node_modules/.bin/fallow");
const KNIP = join(ROOT, "node_modules/.bin/knip");

const SAMPLE_FILES = [
  "src/index.ts",
  "src/App.tsx",
  "src/utils/format.ts",
  "src/utils/math.ts",
  "src/components/Card.tsx",
  "src/components/Banner.tsx",
  "src/services/moduleA.ts",
  "src/services/moduleB.ts",
  "src/legacy/oldHelpers.ts",
  "package.json",
];

const ANSI = /\[[0-9;]*[A-Za-z]/g;
const stripAnsi = (s) => s.replace(ANSI, "");
const sanitize = (s) => s.split(ROOT + "/").join("").split(ROOT).join("");

function run(bin, args) {
  const started = process.hrtime.bigint();
  let stdout = "";
  try {
    stdout = execFileSync(bin, args, { cwd: ROOT, encoding: "utf8", maxBuffer: 64 * 1024 * 1024 });
  } catch (e) {
    stdout = (e.stdout ?? "").toString();
  }
  const ms = Number(process.hrtime.bigint() - started) / 1e6;
  return { stdout, ms };
}

// ---- fallow ----
const fallowJsonRun = run(FALLOW, ["--root", SAMPLE_DIR, "--format", "json"]);
const fj = JSON.parse(fallowJsonRun.stdout);
const fallowHumanRun = run(FALLOW, ["--root", SAMPLE_DIR]);

const BAD_SEVERITY = new Set(["critical", "high", "major", "medium", "warning", "warn"]);

const fallowRaw = {
  check: {
    unused_files: (fj.check?.unused_files ?? []).map((f) => ({ path: f.path })),
    unused_exports: (fj.check?.unused_exports ?? []).map((e) => ({
      path: e.path,
      export_name: e.export_name,
      line: e.line,
    })),
    unused_dependencies: (fj.check?.unused_dependencies ?? []).map((d) => ({
      package_name: d.package_name,
      line: d.line,
    })),
    circular_dependencies: (fj.check?.circular_dependencies ?? []).map((c) => ({
      files: c.files,
      line: c.line,
    })),
  },
  dupes: {
    clone_groups: (fj.dupes?.clone_groups ?? []).map((g) => ({
      instances: g.instances.map((i) => ({
        file: i.file,
        start_line: i.start_line,
        end_line: i.end_line,
      })),
    })),
  },
  health: {
    findings: (fj.health?.findings ?? [])
      .filter((h) => !h.severity || BAD_SEVERITY.has(String(h.severity).toLowerCase()))
      .map((h) => ({
        path: h.path,
        name: h.name,
        line: h.line,
        cyclomatic: h.cyclomatic,
        cognitive: h.cognitive,
        crap: h.crap,
        severity: h.severity,
      })),
  },
  elapsed_ms: fj.elapsed_ms,
};

// File health scores テーブル以降はパネルでは冗長なので落とす。
function trimFallowHuman(text) {
  const lines = sanitize(stripAnsi(text)).split("\n");
  const cut = lines.findIndex((l) => l.includes("File health scores"));
  const kept = cut >= 0 ? lines.slice(0, cut) : lines;
  return kept.join("\n").replace(/\s+$/, "");
}

// ---- knip ----
const knipJsonRun = run(KNIP, [
  "--directory",
  SAMPLE_DIR,
  "--reporter",
  "json",
  "--no-exit-code",
  "--no-progress",
]);
const kj = JSON.parse(knipJsonRun.stdout);
const knipHumanRun = run(KNIP, [
  "--directory",
  SAMPLE_DIR,
  "--no-exit-code",
  "--no-progress",
]);

const knipRaw = {
  issues: (kj.issues ?? []).map((it) => ({
    file: it.file,
    files: it.files ?? [],
    exports: (it.exports ?? []).map((x) => ({ name: x.name, line: x.line })),
    types: (it.types ?? []).map((x) => ({ name: x.name, line: x.line })),
    dependencies: (it.dependencies ?? []).map((x) => ({ name: x.name, line: x.line })),
    devDependencies: (it.devDependencies ?? []).map((x) => ({ name: x.name, line: x.line })),
    duplicates: (it.duplicates ?? []).map((x) => ({ name: x.name, line: x.line })),
  })),
};

// ---- 出力: analysisFixtures.ts ----
const fixtures = {
  fallow: {
    raw: fallowRaw,
    cli: trimFallowHuman(fallowHumanRun.stdout),
    elapsedMs: Math.round(fallowJsonRun.ms),
  },
  knip: {
    raw: knipRaw,
    cli: sanitize(stripAnsi(knipHumanRun.stdout)).replace(/\s+$/, ""),
    elapsedMs: Math.round(knipJsonRun.ms),
  },
};

const fixturesTs = `// AUTOGENERATED by scripts/capture-analysis.mjs — 手で編集しないこと。
// fallow / knip を analysis-sample に実行した「実際の出力」を射影して保存したもの。
import type { FallowRaw, KnipRaw } from "../lib/analysis.ts";

export const analysisFixtures: {
  fallow: { raw: FallowRaw; cli: string; elapsedMs: number };
  knip: { raw: KnipRaw; cli: string; elapsedMs: number };
} = ${JSON.stringify(fixtures, null, 2)};
`;
writeFileSync(join(ROOT, "src/data/analysisFixtures.ts"), fixturesTs);

// ---- 出力: sample.ts ----
const langOf = (p) => (p.endsWith(".tsx") ? "tsx" : p.endsWith(".json") ? "json" : "ts");
const files = SAMPLE_FILES.map((rel) => ({
  path: rel,
  lang: langOf(rel),
  code: readFileSync(join(ROOT, SAMPLE_DIR, rel), "utf8").replace(/\s+$/, "") + "\n",
}));

const sampleTs = `// AUTOGENERATED by scripts/capture-analysis.mjs — 手で編集しないこと。
// analysis-sample/ の中身（= 解析対象そのもの）を表示用に取り込んだもの。
export type SampleLang = "ts" | "tsx" | "json";
export type SampleFile = { path: string; lang: SampleLang; code: string };

export const sampleFiles: SampleFile[] = ${JSON.stringify(files, null, 2)};
`;
mkdirSync(join(ROOT, "src/data"), { recursive: true });
writeFileSync(join(ROOT, "src/data/sample.ts"), sampleTs);

// ---- 検証用サマリ ----
const countByCat = (raw, tool) => {
  const { normalizeFallow, normalizeKnip } = { normalizeFallow: fNorm, normalizeKnip: kNorm };
  const fs = tool === "fallow" ? normalizeFallow(raw) : normalizeKnip(raw);
  const cats = new Set(fs.map((f) => f.category));
  return { total: fs.length, categories: [...cats] };
};
// 簡易ローカル正規化（lib/analysis.ts と同じ規則の最小版。サマリ表示専用）。
function fNorm(raw) {
  const o = [];
  raw.check.unused_files.forEach(() => o.push({ category: "unused-file" }));
  raw.check.unused_exports.forEach(() => o.push({ category: "unused-export" }));
  raw.check.unused_dependencies.forEach(() => o.push({ category: "unused-dependency" }));
  raw.check.circular_dependencies.forEach((c) =>
    c.files.forEach(() => o.push({ category: "circular-dependency" })),
  );
  raw.dupes.clone_groups.forEach((g) =>
    g.instances.forEach(() => o.push({ category: "duplication" })),
  );
  raw.health.findings.forEach(() => o.push({ category: "complexity" }));
  return o;
}
function kNorm(raw) {
  const o = [];
  raw.issues.forEach((it) => {
    it.files.forEach(() => o.push({ category: "unused-file" }));
    [...it.exports, ...it.types].forEach(() => o.push({ category: "unused-export" }));
    [...it.dependencies, ...it.devDependencies].forEach(() =>
      o.push({ category: "unused-dependency" }),
    );
    it.duplicates.forEach(() => o.push({ category: "duplication" }));
  });
  return o;
}

console.log("✓ fixtures written.");
console.log("  fallow:", JSON.stringify(countByCat(fallowRaw, "fallow")));
console.log("  knip:  ", JSON.stringify(countByCat(knipRaw, "knip")));
